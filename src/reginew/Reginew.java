package reginew;

public class Reginew {
	
	private int eventList;
	private String eventName;
	private String content;
	private String brandName;
	private String Id;	
	private String date;
	private String coupon;
	private String reginewImg;
	private String reginewImgDetail;
	private int reginewAvailable;


	public int getEventList() {
		return eventList;
	}
	public void setEventList(int eventList) {
		this.eventList = eventList;
	}

	public String getEventName() {
		return eventName;
	}
	public void setEventName(String eventName) {
		this.eventName = eventName;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getBrandName() {
		return brandName;
	}
	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}
	
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}

	public String getCoupon() {
		return coupon;
	}
	public void setCoupon(String coupon) {
		this.coupon = coupon;
	}
	public int getReginewAvailable() {
		return reginewAvailable;
	}
	public void setReginewAvailable(int reginewAvailable) {
		this.reginewAvailable = reginewAvailable;
	}
	public String getReginewImg() {
		return reginewImg;
	}
	public void setReginewImg(String reginewImg) {
		this.reginewImg = reginewImg;
	}
	public String getReginewImgDetail() {
		return reginewImgDetail;
	}
	public void setReginewImgDetail(String reginewImgDetail) {
		this.reginewImgDetail = reginewImgDetail;
	}



}
