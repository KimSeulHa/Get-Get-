package JEvent;

public class Jevent {
	private int jeventnum;
	private String id;
	private String eventName;
	private String brandName;
	private String jeventimg;
	private int jeventAvailable;
	
	
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getEventName() {
		return eventName;
	}
	public void setEventName(String eventName) {
		this.eventName = eventName;
	}
	public String getBrandName() {
		return brandName;
	}
	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}
	public int getJeventnum() {
		return jeventnum;
	}
	public void setJeventnum(int jeventnum) {
		this.jeventnum = jeventnum;
	}
	public String getJeventimg() {
		return jeventimg;
	}
	public void setJeventimg(String jeventimg) {
		this.jeventimg = jeventimg;
	}
	public int getJeventAvailable() {
		return jeventAvailable;
	}
	public void setJeventAvailable(int jeventAvailable) {
		this.jeventAvailable = jeventAvailable;
	}
	
}
